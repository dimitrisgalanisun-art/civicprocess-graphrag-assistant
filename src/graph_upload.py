import argparse
import json
import re
from pathlib import Path

from neo4j import GraphDatabase

from src.config import (
    NEO4J_DATABASE,
    NEO4J_PASSWORD,
    NEO4J_URI,
    NEO4J_USERNAME,
    QA_JSONL_FILE,
    validate_env,
)


BACKTICK_PATTERN = re.compile(r"`([^`]+)`")


def load_jsonl(path: Path) -> list[dict]:
    """Load private Q/A records from JSONL."""
    if not path.exists():
        raise FileNotFoundError(
            f"JSONL dataset not found: {path}\n"
            "Run: python -m src.build_dataset"
        )

    records = []

    with path.open("r", encoding="utf-8") as file:
        for line in file:
            if line.strip():
                records.append(json.loads(line))

    return records


def extract_keywords(record: dict, max_keywords: int = 25) -> list[str]:
    """
    Extract lightweight graph keywords from private content.

    Priority:
    1. technical_terms generated during dataset building
    2. backticked identifiers, if any remain
    3. snake_case identifiers from question and answer text
    """
    raw_keywords = []

    # 1. Prefer technical terms generated by build_dataset.py
    raw_keywords.extend(record.get("technical_terms", []))

    text = f"{record.get('question', '')}\n{record.get('answer', '')}"

    # 2. Backticked identifiers
    raw_keywords.extend(BACKTICK_PATTERN.findall(text))

    # 3. Snake_case technical identifiers
    raw_keywords.extend(
        re.findall(
            r"\b[A-Za-z][A-Za-z0-9]+_[A-Za-z0-9_]+\b",
            text,
        )
    )

    cleaned = []

    for keyword in raw_keywords:
        keyword = str(keyword).strip().strip(".,;:()[]{}")

        if not keyword:
            continue

        if len(keyword) < 3:
            continue

        if len(keyword) > 80:
            continue

        cleaned.append(keyword)

    # Preserve order while removing duplicates.
    unique_keywords = list(dict.fromkeys(cleaned))

    return unique_keywords[:max_keywords]


def get_driver():
    """Create Neo4j driver."""
    validate_env()

    return GraphDatabase.driver(
        NEO4J_URI,
        auth=(NEO4J_USERNAME, NEO4J_PASSWORD),
    )


def run_write_query(driver, query: str, parameters: dict | None = None):
    """Run a write query against the configured Neo4j database."""
    parameters = parameters or {}

    with driver.session(database=NEO4J_DATABASE) as session:
        session.run(query, parameters)


def run_read_query(driver, query: str, parameters: dict | None = None) -> list[dict]:
    """Run a read query and return dictionaries."""
    parameters = parameters or {}

    with driver.session(database=NEO4J_DATABASE) as session:
        result = session.run(query, parameters)
        return [dict(record) for record in result]


def create_constraints(driver):
    """Create uniqueness constraints for this project graph."""
    constraints = [
        """
        CREATE CONSTRAINT civicrag_record_id IF NOT EXISTS
        FOR (n:CivicRAGRecord)
        REQUIRE n.id IS UNIQUE
        """,
        """
        CREATE CONSTRAINT civicrag_question_id IF NOT EXISTS
        FOR (n:CivicRAGQuestion)
        REQUIRE n.id IS UNIQUE
        """,
        """
        CREATE CONSTRAINT civicrag_answer_id IF NOT EXISTS
        FOR (n:CivicRAGAnswer)
        REQUIRE n.id IS UNIQUE
        """,
        """
        CREATE CONSTRAINT civicrag_source_name IF NOT EXISTS
        FOR (n:CivicRAGSource)
        REQUIRE n.name IS UNIQUE
        """,
        """
        CREATE CONSTRAINT civicrag_section_name IF NOT EXISTS
        FOR (n:CivicRAGSection)
        REQUIRE n.name IS UNIQUE
        """,
        """
        CREATE CONSTRAINT civicrag_keyword_name IF NOT EXISTS
        FOR (n:CivicRAGKeyword)
        REQUIRE n.name IS UNIQUE
        """,
    ]

    for query in constraints:
        run_write_query(driver, query)


def reset_project_graph(driver):
    """
    Delete all existing nodes and relationships in the Neo4j database.

    Use this only when the database is dedicated to this project.
    """
    query = """
    MATCH (n)
    DETACH DELETE n
    """

    run_write_query(driver, query)
    print("Deleted all existing Neo4j nodes and relationships.")


def upload_record(driver, record: dict):
    """Upload one Q/A record and its graph connections."""
    record_id = record["id"]
    question_id = f"{record_id}_question"
    answer_id = f"{record_id}_answer"

    keywords = extract_keywords(record)

    parameters = {
        "record_id": record_id,
        "question_id": question_id,
        "answer_id": answer_id,
        "question": record.get("question", ""),
        "answer": record.get("answer", ""),
        "source": record.get("source", "unknown"),
        "section_title": record.get("section_title", "unknown"),
        "heading_level": record.get("heading_level", 0),
        "chunk_index": record.get("chunk_index", 0),
        "keywords": keywords,
    }

    query = """
    MERGE (r:CivicRAG:CivicRAGRecord {id: $record_id})
    SET
        r.question = $question,
        r.answer = $answer,
        r.source = $source,
        r.section_title = $section_title,
        r.heading_level = $heading_level,
        r.chunk_index = $chunk_index

    MERGE (q:CivicRAG:CivicRAGQuestion {id: $question_id})
    SET q.text = $question

    MERGE (a:CivicRAG:CivicRAGAnswer {id: $answer_id})
    SET a.text = $answer

    MERGE (src:CivicRAG:CivicRAGSource {name: $source})

    MERGE (sec:CivicRAG:CivicRAGSection {name: $section_title})
    SET sec.heading_level = $heading_level

    MERGE (r)-[:HAS_QUESTION]->(q)
    MERGE (r)-[:HAS_ANSWER]->(a)
    MERGE (q)-[:ANSWERED_BY]->(a)
    MERGE (r)-[:FROM_SOURCE]->(src)
    MERGE (r)-[:IN_SECTION]->(sec)

    WITH r, $keywords AS keywords
    UNWIND keywords AS keyword
        MERGE (k:CivicRAG:CivicRAGKeyword {name: keyword})
        MERGE (r)-[:MENTIONS]->(k)
    """

    run_write_query(driver, query, parameters)


def upload_records(driver, records: list[dict]):
    """Upload all records."""
    for index, record in enumerate(records, start=1):
        upload_record(driver, record)

        if index % 25 == 0:
            print(f"Uploaded {index}/{len(records)} records...")

    print(f"Uploaded {len(records)} records.")


def print_graph_summary(driver):
    """Print basic graph counts."""
    query = """
    MATCH (n:CivicRAG)
    RETURN labels(n) AS labels, count(n) AS count
    ORDER BY labels
    """

    rows = run_read_query(driver, query)

    print("\nGraph summary:")
    for row in rows:
        print(f"{row['labels']}: {row['count']}")

    rel_query = """
    MATCH (:CivicRAG)-[r]->(:CivicRAG)
    RETURN type(r) AS relationship, count(r) AS count
    ORDER BY relationship
    """

    rel_rows = run_read_query(driver, rel_query)

    print("\nRelationship summary:")
    for row in rel_rows:
        print(f"{row['relationship']}: {row['count']}")


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--reset",
        action="store_true",
        help="Delete existing CivicRAG graph before upload.",
    )
    parser.add_argument(
        "--upload",
        action="store_true",
        help="Upload private JSONL records to Neo4j.",
    )
    parser.add_argument(
        "--summary",
        action="store_true",
        help="Print graph summary.",
    )

    args = parser.parse_args()

    driver = get_driver()

    try:
        driver.verify_connectivity()
        print("Connected to Neo4j.")

        create_constraints(driver)

        if args.reset:
            reset_project_graph(driver)

        if args.upload:
            records = load_jsonl(QA_JSONL_FILE)
            print(f"Loaded {len(records)} records from {QA_JSONL_FILE}")
            upload_records(driver, records)

        if args.summary:
            print_graph_summary(driver)

        if not args.reset and not args.upload and not args.summary:
            parser.print_help()

    finally:
        driver.close()


if __name__ == "__main__":
    main()
